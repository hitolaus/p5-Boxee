=head1 NAME

Boxee - Boxee library for Perl

=head1 SYNOPSIS

use Boxee;

=head1 DESCRIPTION

Boxee provides a library for accessing Boxee APIs.

=head1 INSTALLATION

Once all dependencies have been resolved, C<Boxee> installs with
the typical sequence

perl Makefile.PL
make
make test
make install

